# Using for loop with range
print("10 to 1 in for loop")
# rangeâ†’ start at 10, stop at 0, step -1
for i in range(10, 0, -1):
    #Print the current number
    print(i)

# After the loop ends, print "Blastoff!"
print("Blastoff!")

# Using while loop with range
print("10 to 1 in while loop")
#Start with the number 10
i = 10

#Continue looping while i is greater than or equal to 1
while i >= 1:
    # Print the current number
    print(i)
    
    # Decrease i by 1 in each iteration
    i -= 1

# After the loop ends, print "Blastoff!"
print("Blastoff!")